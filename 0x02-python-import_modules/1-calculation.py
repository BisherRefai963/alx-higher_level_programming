#!/usr/bin/python3
def add(k, l):
    """My addition function

    Args:
        k: first integer
        l: second integer

    Returns:
        The return value. a + b
    """
    return (k + l)


def sub(k, l):
    """My subtraction function

    Args:
        k: first integer
        l: second integer

    Returns:
        The return value. a - b
    """
    return (k - l)


def mul(k, l):
    """My multiplication function

    Args:
        k: first integer
        l: second integer

    Returns:
        The return value. k * l
    """
    return (k * l)


def div(k, l):
    """My division function

    Args:
        k: first integer
        l: second integer

    Returns:
        The return value. k / l
    """
    return int(k /l)

